#!/usr/bin/env node
/**
 * Generate `src-tauri/Info.ios.plist`, which Tauri v2 auto-merges into the iOS app's `Info.plist`
 * at `tauri ios dev|build` time (additive for new keys; no config wiring needed).
 *
 * It adds two things the generated project doesn't have:
 * - `UIBackgroundModes: [audio]` — lets discovery preview playback continue in the background (#79).
 * - `CFBundleURLTypes` — registers the Google OAuth callback URL scheme (the *reversed client id*)
 *   for native cloud sign-in (#133), matching the iOS OAuth client in the Google Cloud console.
 *
 * `src-tauri/gen/apple` is gitignored (regenerated by `cargo tauri ios init`), so editing the
 * generated plist wouldn't persist — this merge file is the supported mechanism.
 *
 * The output is itself gitignored (it embeds the iOS OAuth client id, kept out of git exactly like
 * `cloud_sync.config.json`). It is fully derived, so it's overwritten on every run.
 *
 * Client-id resolution mirrors `services/cloud_sync/config.rs` (file first, then compile-time env):
 *   1. `ios_oauth_client_id` from `src-tauri/cloud_sync.config.json` (local dev)
 *   2. `GCLOUD_IOS_OAUTH_CLIENT_ID` env var (CI / release builds)
 *
 * Safe by design: a missing/blank/malformed config or absent env var is not fatal — the plist is
 * still written with `UIBackgroundModes` and a warning that the OAuth redirect isn't registered, so
 * the build proceeds (exit 0), symmetric with `scripts/write-cloud-config.mjs`.
 *
 * Usage: node scripts/write-ios-plist.mjs
 */
import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const configPath = resolve(repoRoot, 'src-tauri/cloud_sync.config.json')
const outPath = resolve(repoRoot, 'src-tauri/Info.ios.plist')

/**
 * Derive a Google OAuth client's reversed-client-id URL scheme from its client id, e.g.
 * `123-abc.apps.googleusercontent.com` -> `com.googleusercontent.apps.123-abc`. Must match
 * `reversed_client_id` in `src-tauri/src/services/cloud_sync/config.rs`.
 */
function reversedClientId(clientId) {
	const suffix = '.apps.googleusercontent.com'
	const head = clientId.endsWith(suffix) ? clientId.slice(0, -suffix.length) : clientId
	return `com.googleusercontent.apps.${head}`
}

/** Resolve the iOS OAuth client id: config file first, then env. Returns a trimmed string or null. */
function resolveIosClientId() {
	try {
		const config = JSON.parse(readFileSync(configPath, 'utf8'))
		const fromFile = config.ios_oauth_client_id
		if (typeof fromFile === 'string' && fromFile.trim() !== '') return fromFile.trim()
	} catch (err) {
		if (err.code !== 'ENOENT') {
			console.warn(`[write-ios-plist] could not read ${configPath}: ${err.message}`)
		}
	}
	const fromEnv = process.env.GCLOUD_IOS_OAUTH_CLIENT_ID
	if (fromEnv && fromEnv.trim() !== '') return fromEnv.trim()
	return null
}

const clientId = resolveIosClientId()

const urlTypesBlock =
	clientId === null
		? ''
		: `	<key>CFBundleURLTypes</key>
	<array>
		<dict>
			<key>CFBundleTypeRole</key>
			<string>Editor</string>
			<key>CFBundleURLName</key>
			<string>com.bbx-audio.crate.oauth</string>
			<key>CFBundleURLSchemes</key>
			<array>
				<string>${reversedClientId(clientId)}</string>
			</array>
		</dict>
	</array>
`

const plist = `<?xml version="1.0" encoding="UTF-8"?>
<!DOCTYPE plist PUBLIC "-//Apple//DTD PLIST 1.0//EN" "http://www.apple.com/DTDs/PropertyList-1.0.dtd">
<plist version="1.0">
<dict>
	<key>UIBackgroundModes</key>
	<array>
		<string>audio</string>
	</array>
${urlTypesBlock}</dict>
</plist>
`

writeFileSync(outPath, plist)

if (clientId === null) {
	console.warn(
		'[write-ios-plist] no iOS OAuth client id (src-tauri/cloud_sync.config.json#ios_oauth_client_id ' +
			'or GCLOUD_IOS_OAUTH_CLIENT_ID) — wrote Info.ios.plist with background audio only; the OAuth ' +
			'redirect scheme is not registered and mobile sign-in would fail',
	)
} else {
	console.log(`[write-ios-plist] wrote ${outPath} (UIBackgroundModes: audio, OAuth redirect scheme registered)`)
}
