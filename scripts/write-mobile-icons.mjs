#!/usr/bin/env node
/**
 * Materialize the channel-specific mobile app icons into the generated native projects
 * (`src-tauri/gen/apple` and `src-tauri/gen/android`) before a `tauri ios|android dev|build`.
 *
 * Why a prebuild step instead of config? Desktop varies its icon per channel through each config's
 * `bundle.icon` array (`icons/{dev,staging,prod}/...`), but Tauri mobile has no equivalent — the iOS
 * asset catalog and the Android `res/mipmap-*` resources are read straight from the gen projects at
 * build time. So the only way to ship a dev/staging/prod icon on mobile is to copy the right set into
 * the gen project first. The branded per-channel sets already live in `src-tauri/icons/{channel}/{ios,
 * android}/` (generated by `tauri icon`); this script just selects one and stamps it into gen.
 *
 * The icon files it writes under gen are gitignored (see `src-tauri/.gitignore`) — `icons/{channel}/`
 * is the source of truth and these are derived artifacts, exactly like `Info.ios.plist` and
 * `cloud_sync.config.json`. That keeps `yarn dev:ios`/`dev:android` from churning the working tree.
 *
 * Channel resolution: `--channel <dev|staging|prod>` wins; else `CRATE_ENV`
 * (development|staging|production) is mapped; else defaults to `prod` (the release identity).
 *
 * Usage:
 *   node scripts/write-mobile-icons.mjs [--platform ios|android|all] [--channel dev|staging|prod]
 */
import { cpSync, existsSync, readdirSync, statSync } from 'node:fs'
import { dirname, join, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const iconsRoot = resolve(repoRoot, 'src-tauri/icons')
const iosDest = resolve(repoRoot, 'src-tauri/gen/apple/Assets.xcassets/AppIcon.appiconset')
const androidResDest = resolve(repoRoot, 'src-tauri/gen/android/app/src/main/res')

/** Read the value following a `--flag` in argv, or null. */
function getArg(name) {
	const i = process.argv.indexOf(name)
	return i !== -1 && i + 1 < process.argv.length ? process.argv[i + 1] : null
}

// Accept both the short folder names and the CRATE_ENV names, so callers can pass either.
const CHANNEL_BY_KEY = {
	dev: 'dev',
	development: 'dev',
	staging: 'staging',
	prod: 'prod',
	production: 'prod',
}

/** Resolve the channel: explicit `--channel`, else CRATE_ENV, else `prod`. */
function resolveChannel() {
	const fromArg = getArg('--channel')
	if (fromArg) {
		const channel = CHANNEL_BY_KEY[fromArg.toLowerCase()]
		if (!channel) {
			console.error(`[write-mobile-icons] unknown --channel "${fromArg}" (expected dev|staging|prod)`)
			process.exit(1)
		}
		return channel
	}
	const fromEnv = process.env.CRATE_ENV
	if (fromEnv && CHANNEL_BY_KEY[fromEnv.toLowerCase()]) return CHANNEL_BY_KEY[fromEnv.toLowerCase()]
	return 'prod'
}

/** Count files in a directory tree, for logging. */
function countFiles(dir) {
	let n = 0
	for (const entry of readdirSync(dir, { withFileTypes: true })) {
		const full = join(dir, entry.name)
		if (entry.isDirectory()) n += countFiles(full)
		else if (entry.isFile()) n += 1
	}
	return n
}

const platform = (getArg('--platform') || 'all').toLowerCase()
if (!['ios', 'android', 'all'].includes(platform)) {
	console.error(`[write-mobile-icons] unknown --platform "${platform}" (expected ios|android|all)`)
	process.exit(1)
}
const channel = resolveChannel()

/**
 * Merge-copy a channel's icon set into a gen destination. `cpSync({recursive})` mirrors the source
 * tree into the destination (creating dirs like Android's `mipmap-anydpi-v26/`) and overwrites
 * matching files, while leaving sibling files untouched — so iOS keeps its `Contents.json` and
 * Android keeps `colors.xml`/`strings.xml`/`themes.xml`.
 */
function stamp(label, src, dest) {
	if (!existsSync(src) || !statSync(src).isDirectory()) {
		console.error(`[write-mobile-icons] missing source icons: ${src}`)
		process.exit(1)
	}
	cpSync(src, dest, { recursive: true, force: true })
	console.log(`[write-mobile-icons] ${label}: copied ${countFiles(src)} file(s) from ${channel} → ${dest}`)
}

if (platform === 'ios' || platform === 'all') {
	stamp('iOS', resolve(iconsRoot, channel, 'ios'), iosDest)
}
if (platform === 'android' || platform === 'all') {
	stamp('Android', resolve(iconsRoot, channel, 'android'), androidResDest)
}
